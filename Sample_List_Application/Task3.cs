	void reset(Object sender,EventArgs e) 
	{ 
		txt.Text = "";
		txt2.Text = "";
	}
	
	void read(Object sender,EventArgs e) 
	{ 
		string[] sep = new string[] {"\r\n"};
		string[] lines = txt.Text.Split(sep, StringSplitOptions.RemoveEmptyEntries);
		txt2.Text = lines[0] +","+ lines[1];
	}
	
	void sum(Object sender,EventArgs e) 
	{ 
		string[] sep = new string[] {"\r\n"};
		string[] lines = txt.Text.Split(sep, StringSplitOptions.RemoveEmptyEntries);
		int num1 = Convert.ToInt32(lines[0]);
		int num2 = Convert.ToInt32(lines[1]);
		int sum = num1 + num2;
		txt2.Text = Convert.ToString(sum);
	}
	
	void product(Object sender,EventArgs e) 
	{ 
		string[] sep = new string[] {"\r\n"};
		string[] lines = txt.Text.Split(sep, StringSplitOptions.RemoveEmptyEntries);
		int num1 = Convert.ToInt32(lines[0]);
		int num2 = Convert.ToInt32(lines[1]);
		int sum = num1 * num2;
		txt2.Text = Convert.ToString(sum);
	}
	
	void mean(Object sender,EventArgs e) 
	{ 
		string[] sep = new string[] {"\r\n"};
		string[] lines = txt.Text.Split(sep, StringSplitOptions.RemoveEmptyEntries);
		int num1 = Convert.ToInt32(lines[0]);
		int num2 = Convert.ToInt32(lines[1]);
		int sum = (num1 + num2)/2;
		txt2.Text = Convert.ToString(sum);
	}
	
